const calculator = {
    add : function(a, b){
        return a+b;
    },

    minus : function(a, b){
        return a-b;
    },

    divide : function(a, b){
        return a/b;
    },

    powerOf : function(a, b){
        return a**b;
    },

}

calculator.add(97, 22);
calculator.divide(21, 3);
calculator.minus(50, 48);
calculator.powerOf(2, 2);